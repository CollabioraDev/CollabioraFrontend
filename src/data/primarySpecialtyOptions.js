/** Grouped by parent specialty (text after comma) for CustomSelect dropdown */
export const primarySpecialtyOptionGroups = (() => {
  const groups = {};
  for (const opt of flatOptions) {
    const commaIdx = opt.indexOf(", ");
    const group =
      commaIdx >= 0 ? opt.slice(commaIdx + 2).trim() : "Primary Specialties";
    if (!groups[group]) groups[group] = [];
    groups[group].push(opt);
  }
  return Object.entries(groups)
    .sort((a, b) => {
      if (a[0] === "Primary Specialties") return -1;
      if (b[0] === "Primary Specialties") return 1;
      return a[0].localeCompare(b[0]);
    })
    .map(([group, options]) => ({ group, options }));
})();
