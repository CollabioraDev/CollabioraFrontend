export function normalizeMedToken(s) {
  return String(s || "")
    .trim()
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function findMedicationByLabel(label) {
  const n = normalizeMedToken(label);
  return MEDICATION_GENERICS.find((m) => m.search_terms.includes(n));
}
