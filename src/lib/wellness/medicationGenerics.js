/**
 * Local medication autocomplete catalog.
 *
 * Canonical generic names are stored in `label`; brand names, regional names,
 * and common aliases are stored as synonyms/search terms for fuzzy search.
 * Categories support supplement interaction checks.
 */

/**
 * @typedef {{
 *   id: string;
 *   name: string;
 *   label: string;
 *   category: string;
 *   synonyms: string[];
 *   search_terms: string[];
 *   common: boolean;
 * }} MedicationGeneric
 */

function normalizeMedId(value) {
  return normalizeMedToken(value).replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function m(name, category, synonyms = [], common = false) {
  const label = normalizeMedToken(name);
  const cleanSynonyms = synonyms.map(normalizeMedToken).filter(Boolean);
  const searchTerms = Array.from(new Set([label, ...cleanSynonyms]));
  return {
    id: normalizeMedId(label),
    name: label,
    label,
    category,
    synonyms: cleanSynonyms,
    search_terms: searchTerms,
    common,
  };
}

